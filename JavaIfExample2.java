package devsought.ifelse;

public class JavaIfExample2 {

    public static void main(String... args) {
        int a = 8;
        int b = 6;
        if (a < b) {
            System.out.println(a + " is less than " + b);
        } else {
            System.out.println(a + " is greater than " + b);
        }

    }
}
